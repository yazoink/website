<p>
  If you traveled to the end of every individual instance of consciousness,
  would you always, ultimately, end up in the same place?
</p>
<br />
<p>
  In <i>The Doors of Perception</i>, Aldous Huxley documented his first
  experience with the hallucinogen, mescalin, along with a variety of pertaining
  revelations. He was thrown into a world which was completely irrational within
  the realm of ordinary human perception.
</p>
<br />
<p>
  The principal theory of <i>The Doors of Perception</i> is that the brain and
  nervous system are primarily eliminative in function, serving as a filtration
  system, or “reducing valve” for a collective consciouness or “Mind at Large”,
  which constitutes the raw materials of the universe. The idea of a collective
  consciousness is not new or unique to Huxley by any means, but I would like to
  use <i>The Doors of Perception</i> as a point of entry into these ideas.
</p>
<br />
<p>
  The reducing valve is a mechanism of individualisation; without it, you would
  be capable of remembering everything that has ever happened and perceiving
  everything that is happening everywhere in the universe. The brain and nervous
  system serve to protect us from being overwhelmed and confused by this hoard
  of largely useless stimuli by narrowing the field of perception to a very
  small selection of what is practically useful.
</p>
<br />
<p>
  Imagine perceiving <i>everything</i> -- what would you do? Probably not much.
  It would be quite overwhelming, to say the least.
</p>
<br />
<p>
  The reducing valve theory could explain why certain animals only possess the
  ability to see certain colours, or why some have evolved to possess sharper
  senses of smell or hearing. We know that there are more colours, noises, and
  smells “out there” than we have the capacity to percieve, thus there must be
  some sort of <i>barrier</i> sheltering us from this superfluous data.
</p>
<br />
<div class="content-indent" id="quote-3">
  <p>
    For certain animals it is biologically very important to be able to
    distinguish certain hues. But beyond the limits of their utilitarian
    spectrum, most creatures are completely color blind. Bees, for example,
    spend most of their time “deflowering the fresh virgins of the spring” but,
    as Von Frisch has shown, they can recognize only a very few colors. Man’s
    highly developed color sense is a biological luxury—inestimably precious to
    him as an intellectual and spiritual being, but unnecessary to his survival
    as an animal.<sup>
      <a href="?nav=writing&piece=doors-in-the-wall:-absurdism-vs.-the-doors-of-perception&q=3&f=3#footnote-3">3</a>
    </sup>
  </p>
</div>
<br />
<p>
  You hear people talking on the other side of the wall, but you can’t make out
  exactly what they’re saying… You catch the odd word, but not enough to make
  any sense of the conversation. If you had the ears of, say, a cat, would you
  be able to hear enough to decipher what they’re saying? There’s a good chance
  that you would. The knowledge is out there -- it’s just beyond the range of
  your body.
</p>
<br />

<p>
  Huxley suggests that psychoactive substances, such as mescalin, psylocybin, or
  LSD, “trance-like” states (i.e. meditation, hypnosis), psychological
  conditions such as schizophrenia, and physical circumstances under which the
  body is subjected to physical stress all serve to loosen the reducing valve.
  The states achieved under these conditions all bear remarkable similarity.
</p>
<br />
<div class="content-indent" id="quote-4">
  <p>
    The brain is provided with a number of enzyme systems which serve to
    co-ordinate its workings. Some of these enzymes regulate the supply of
    glucose to the brain cells. Mescalin inhibits the production of these
    enzymes and thus lowers the amount of glucose available to an organ that is
    in constant need of sugar. When mescalin reduces the brain’s normal ration
    of sugar what happens?<sup>
      <a href="?nav=writing&piece=doors-in-the-wall:-absurdism-vs.-the-doors-of-perception&q=4&f=3#footnote-3">3</a>
    </sup>
  </p>
</div>
<br />
<p>
  The efficacy of the reducing valve is equivalent to the wellbeing of the
  physical body. You come down with a fever and your dreams start to play
  bizarre, delerious mind games -- migrane closing in with floating shapes,
  zig-zagging lines… flashing lights…
</p>
<br />
<p>
  Huxley points out that, in the past, almost all aspirants to the spiritual
  life have engaged in regular courses of bodily mortification (i.e. whipping,
  fasting), in which large quantities of histamine and adrenaline were be
  produced. He suggests that this may be related to the discovery that a product
  of the decomposition of adrenalin is capable of producing many of the symptoms
  observed in mescalin intoxication and schizophrenia. Huxley notes a striking
  similarity between the chemical compositions of mescalin and adrenaline, and
  that antihistamines can be used to treat some symptoms of schizophrenia.
</p>
<br />
<div class="content-indent" id="quote-5">
  <p>
    Fasting was not the only form of physical mortification resorted to by the
    earlier aspirants to spirituality. Most of them regularly used upon
    themselves the whip of knotted leather or even of iron wire. These beatings
    were the equivalent of fairly extensive surgery without anesthetics, and
    their effects on the body chemistry of the penitent were considerable. Large
    quantities of histamine and adrenalin were released while the whip was
    actually being plied; and when the resulting wounds began to fester (as
    wounds practically always did before the age of soap), various toxic
    substances, produced by the decomposition of protein, found their way into
    the blood stream. But histamine produces shock, and shock affects the mind
    no less profoundly than the body. Moreover, large quantities of adrenalin
    may cause hallucinations, and some of the products of its decomposition are
    known to induce symptoms resembling those of schizophrenia. As for toxins
    from wounds—these upset the enzyme systems regulating the brain, and lower
    its efficiency as an instrument for getting on in a world where the
    biologically fittest survive.<sup>
      <a href="?nav=writing&piece=doors-in-the-wall:-absurdism-vs.-the-doors-of-perception&q=5&f=3#footnote-3">3</a>
    </sup>
  </p>
</div>
<br />
<p>
  It is well known that psychedelics induce “neuroplasticity” -- the loosening
  of the brain’s established structure, and the elevated potential for
  reconfiguration. They serve to open the mind to possibilities that were
  otherwise out of scope. The brain on psychedelics lights up like a Christmas
  tree -- <i>buzzing</i> with electricity. The activity in the Default Mode
  Network is dulled, opening the door for more, and more flow across different
  networks.
</p>
<br />
<p>
  Psychedelics expand the perception. You will see colour with a depth that you,
  otherwise, never could've fathomed. You will see the world with a remarkable
  intricacy that would appear impossible to the regular human perception. They
  are unlike other drugs in that they do not impair the intellect, instead
  serving to <i>amplify</i> it -- often to a degree to which the senses are so
  overwhelmed that reality ceases to make sense as a solid linear structure.
</p>
<br />
<p>
  Under ordinary circumstances, the individual usually feels that their “self”
  is constituted exclusively by their ego, or some combination of ego and
  physical body. Under the influence of psychedelics, the individual
  conceptualisation of “self” expands out into its surroundings, beginning to
  include a much wider array of concepts than it previously did. With this, the
  ego begins to dissipate. The inevitable conclusion of this ego dissolution is
  the “ego death”, which is characterised by a complete loss of subjective
  self-identity.
</p>
<br />
<p>
  Large doses of psychedelics entail catatonia and complete dissolution of the
  ego. Perception eventually expands to a point at which the physical body no
  longer seems “relevant”. Catatonia and ego dissolution go hand-in-hand -- what
  is the ego without the body? The body has no purpose other than to serve the
  physical individual -- the “individual” is entirely physical.
</p>
<br />

<p>
  You know the back of your hand like the back of your hand. You look at it and
  see every single detail -- every crease… every protruding vein and bone, scar,
  mole... the lengths and widths of the nails... You see the skin’s texture in
  all its intricacy, like a neverending maze. You see all the individual
  creases, and between each crease is a degree of detail is tantamount to the
  number of numbers that you can count. You keep on noticing more and more as
  you focus your attention.
</p>
<br />
<p>
  Your whole being is focused on the back of your hand -- this hand that belongs
  to “you”. You know that it is your hand because you can feel it and move it,
  yet it feels somewhat distant. Your body gets sick sometimes, but you never
  asked it to do that. You may sometimes wish that you were a different height,
  or that you had a different eye colour. There appears to be a distinct
  disconnect between the mind and body. What exactly comprises “you”? Is it your
  consciousness? Your mind, memories, ideas? The role you play in society? Your
  body? All of the above? If the “self” is a self-contained system, how could
  you explain this incongruence between mind and body?
</p>
<br />
<p>
  And where do dreams come from? Those random thoughts that pop up when you’re
  tired? That random tune you find yourself tapping on the table? Do they come
  from within? In <i>The Myth of Sisyphus</i>, Camus established the idea of an
  "unknowable infinity" within man, which allows for original thought and
  creativity -- we could assume that this is where all these “out-there”
  products of the mind originate. But are we operating within separate
  closed-off infinities?
</p>
<br />
<p>
  We know that our senses can be rather inaccurate, and the world that we
  percieve is not the same as the world that everybody else percieves, yet we
  are still able to find common ground, and come to some general consensus.
  Although, it may very well be true that I am not in fact writing this, and I
  am a brain in a vat, hooked up to some kind of stimulation gadget, but if you
  are truly reading this, then it can be assumed that we have some common
  understanding. We have some understanding of “out there” -- beyond the “self”.
</p>
<br />
<p>
  When you focus your attention, you can attune to more and more detail within
  your surroundings. Under the influence of psychedelics, you see more and more
  detail -- more than you ever could’ve dreamed... You get ear surgery and can
  now hear a cacophony sounds that would’ve previously evaded you; you get eye
  surgery and suddenly the world is clear.
</p>
<br />
<p>
  It seems logical to say that infinity comes from without, not from within. If
  infinity came from within, I would be shouting into a void, and you would not
  be reading this.
</p>
<br />
<p>
  The physical body stores the neural pathways that constitute the way that you
  think. It stores your memories, likes, dislikes, fears, sorrows, ecstacies,
  mechanisms… and when it is altered “you” will change. The body physically
  changes with the mind. It could be said that the body is the machine, and the
  infinite is the spark. Plug in the body, and You are born.
</p>
