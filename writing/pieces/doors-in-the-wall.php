      </sup>
  </p>
</div>
<br />
<p>
  The world that you knew ceases to have any reason or meaning; all the systems
  you spent your life learning no longer have any basis. The ABCs turned out to
  be completely arbitrary.
</p>
<br />
<div class="content-indent" id="quote-2">
  <p>
    A step lower and strangeness creeps in: perceiving that the world is
    “dense,” sensing to what a degree a stone is foreign and irreducible to us,
    with what intensity nature or a landscape can negate us. At the heart of all
    beauty lies something inhuman, and these hills, the softness of the sky, the
    outline of these trees at this very minute lose the illusory meaning with
    which we had clothed them, henceforth more remote than a lost paradise. The
    primitive hostility of the world rises up to face us across millennia. For a
    second we cease to understand it because for centuries we have understood in
    it solely the images and designs that we had attributed to it beforehand,
    because henceforth we lack the power to make use of that artifice. The world
    evades us because it becomes itself again. That stage scenery masked by
    habit becomes again what it is. It withdraws at a distance from us. Just as
    there are days when under the familiar face of a woman, we see as a stranger
    her we had loved months or years ago, perhaps we shall come even to desire
    what suddenly leaves us so alone. But the time has not yet come. Just one
    thing: that denseness and that strangeness of the world is the Absurd.<sup
      >
      <a href="?nav=writing&piece=doors-in-the-wall:-absurdism-vs.-the-doors-of-perception&q=2&f=2#footnote-2">2</a>
    </sup>
  </p>
</div>
<br />
<p>
  Everything appears naked -- no connotations or hidden messages. You may say
  that it has a certain quality of <i>suchness</i>... But nothing you see or
  think makes any sense anymore. You think that you may be going crazy.
</p>
<br />
<p>
  You start looking for facts; there must be some solid ground to stand on here.
  You need to know the truth. Or at least one truth, to start with...
</p>
<br />
<p>
  You keep on asking <i>why</i> until you reach the eternal question mark -- the
  limits of human perception. You realise that it's impossible to really
  <i>know</i>
  anything beyond the reaches of your brain and sensory system. You can't prove
  anything except for what is immediately in front of you -- everything else is
  a patchwork of approximation. Camus dubs these limits the "Absurd Walls".
</p>
<br />
<p>
  You keep asking questions until you reach a gaping void. We have no
  foundational knowledge of <i>anything whatsoever</i>. How can we, then, claim
  anything as concrete truth?
</p>
<br />
<p>
  You see the human world laid out in man-made structures: language,
  organisations, dogmas, ideologies, in-groups, out-groups, categories,
  workdays, weekends, holidays... all of them completely arbitrary and
  intangible. The world that you previously knew has crumbled into semantics.
  The truth you are looking for lies beyond the walls of your human awareness.
  You have this inexplicable drive to know more, yet by design you cannot know
  more.
</p>
<br />
<p>Everything ends in a paradox -- this is the Absurd.</p>
<br />
<p>
  You realise that you truly have no way of knowing what happens after you die.
  There is no evidence of any higher purpose or reason. All you want is a higher
  reason or purpose; what else could constitute a reason for living? This is the
  point at which you may take a "leap of faith" and put a name, such as "God",
  to that elusive more;
  <i>"whatever lies beyond those walls must know what it's doing..."</i> Camus
  dubs this act a "negation", or an "elusion" of the Absurd. The Absurd is no
  longer the Absurd once you begin to make assumptions. To stay in the Absurd,
  you must sit with it.
</p>
