<p>
  When you superimpose the heightened “Visionary” state described by Huxley upon
  the Absurd, you can observe many points of overlap. In fact, they share the
  same key features:
</p>
<br />
<ol>
  <li>
    <p>
      The world appears in phenomena and phantasmagoria, as opposed to reason or
      structure.
    </p>
  </li>
  <li>
    <p>
      Semantics and implied meaning fall away -- the world appears “naked”,
      without pretension or concrete definition.
    </p>
    <div class="content-indent" id="quote-6">
      <p>
        To formulate and express the contents of this reduced awareness, man has
        invented and endlessly elaborated those symbol-systems and implicit
        philosophies which we call languages. Every individual is at once the
        beneficiary and the victim of the linguistic tradition into which he has
        been born—the beneficiary inasmuch as language gives access to the
        accumulated records of other people’s experience, the victim in so far
        as it confirms him in the belief that reduced awareness is the only
        awareness and as it bedevils his sense of reality, so that he is all too
        apt to take his concepts for data, his words for actual things.<sup
        >
      <a href="?nav=writing&piece=doors-in-the-wall:-absurdism-vs.-the-doors-of-perception&q=6&f=3#footnote-3">3</a>
        </sup
        >
      </p>
    </div>
  </li>
  <li>
    <p>
      The “walls” that constitute the bounds of human perception become
      apparent.
    </p>
  </li>
  <li>
    <p>
      The “walls” are attributed to the reaches of the brain and sensory system.
    </p>
  </li>
</ol>
<br />
<p>
  It should also be noted that both Huxley and Camus imply religious thought as
  interpretation, formulated within the realm of ordinary human perception, of
  the phenomena beyond the “walls”. Nonetheless, it is clear that Huxley is
  describing a state that fits Camus’ description of Absurdity. From a purely
  Absurdist perspective, the <i>meaning</i>, or <i>explanation</i> that Huxley
  attributes to this state would serve as a “negation” because it is a “leap of
  faith” -- it cannot be <i>proven</i>. Religious and spiritual belief is a
  direct product of the desire for higher meaning, and belief in any higher
  power or reason negates the Absurd.
</p>
<br />
<p>
  In <i>The Myth of Sisyphus</i>, Camus makes examples of Heidigger, Jaspers,
  Chestov, Kierkegard and Husserl as thinkers who have ventured into the Absurd
  and negated it through various means such as spirituality or religion. I would
  say that, without any doubt, Huxley could be placed amongst these thinkers,
  although he introduces a unique factor.
</p>
<br />
<p>
  Camus describes the “Absurd Walls”, which serve as a barrier between human and
  higher perception; we can realise that there is an infinite world beyond our
  senses, but we cannot venture further than our limited scope of perception
  will allow us. The unique factor that Huxley introduces is the suggestion that
  this barrier is malleable, and we have the means to manipulate it -- although,
  it must be said that the discoveries apparent in these experiences are only
  accessible within an active “visionary” state (otherwise they are only
  memories and approximations). But it is possible to breach the walls of
  Absurdity, and there are biological methods by which the boundaries of the
  human perception can be manipulated. Camus’ “Absurd Walls”, and Huxley’s
  “Reducing Valve” appear to be one and the same.
</p>
<br />
<p>
  Taking Huxley's experiences and findings into account, it makes you wonder:
  how much of this is truly negation?
</p>
<br />
<p>
  Huxley describes assimilation with the Mind at Large, via the effects of
  mescalin intoxication in his case, as an experience equivalent to Heaven;
  equating it to the Buddhist idea of the Dharma-Body. He seemed to be under the
  impression that he had encountered some kind of all-encompassing Godly force
  when the reduction valve was loosened -- this is where the “negation” truly
  comes into play. But I would like to ask: who's to say that Huxley didn't
  truly glimpse Heaven? You can’t <i>prove</i> that he didn’t.
</p>
<br />
<p>
  Let me make myself clear: Camus says that the knowledge of the universe is
  beyond the walls of human perception, but again, he did not consider if it was
  possible to <i>manipulate</i> or <i>breach</i> those walls. The Absurd does
  not take this possibility into account. Huxley saw beyond the walls and
  described a Heavenly force; the dissolution of the Individual and assimilation
  into One.
</p>
<br />
<p>
  Camus puts forth the idea that the universe can never be explained in human
  terms, and this would remain true within Huxley's philosophy. Huxley puts
  forth that as one ventures into the Other World and the self dissipates, the
  universe <i>makes itself apparent</i>. Full assimilation with the eternal
  within the confines of a human body would likely be impossible. The whole of
  the universe could likely only make itself apparent through death -- full
  departure from the physical body.
</p>
<br />
<p>
  Thus, it very well may still stand that within our human lifespans, the final
  secret of the universe will remain unknown. Even if this is so, we are still
  capable of accessing a large amount of it through various bodily states. But
  regardless, the experiences of the more “other” states cannot be explained or
  understood in the more “human” states because the nature of the “human” states
  are of abstraction and structure. The eternal is incompatible with the
  language of the human and can only be described through estimates and at
  second-hand. But the line at which human knowledge ends is not solid. Human
  knowledge no longer stops at a wall, but stretches as far as the body's
  ephemeral condition will allow it. The line between within and without is no
  longer a line, but a homeostatic spectrum. You could say that the Walls have
  Doors…
</p>
